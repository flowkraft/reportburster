package com.flowkraft.jobson.api.v1;

import com.fasterxml.jackson.annotation.JsonProperty;

import io.swagger.v3.oas.annotations.media.Schema;

@Schema(description = "Summary information for a user")
public final class APIUserDetails {

    @Schema(description = "A unique identifier for the user", example = "akewley")
    @JsonProperty
    private UserId id;



    /**
     * @deprecated Used by JSON deserializer
     */
    public APIUserDetails()  {}

    public APIUserDetails(UserId id) {
        this.id = id;
    }



    public UserId getId() {
        return id;
    }



    @Override
    public boolean equals(Object o) {
        if (this == o) return true;
        if (o == null || getClass() != o.getClass()) return false;

        APIUserDetails that = (APIUserDetails) o;

        return id != null ? id.equals(that.id) : that.id == null;

    }

    @Override
    public int hashCode() {
        return id != null ? id.hashCode() : 0;
    }
}
